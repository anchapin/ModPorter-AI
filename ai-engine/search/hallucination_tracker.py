@dataclass
class HallucinatedComponent:
    """A detected hallucinated component."""

    component_name: str
    line_number: int
    context: str
    confidence: float
    hallucination_type: str


@dataclass
class HallucinationReport:
    """Report of hallucination analysis."""

    total_components: int
    hallucinated_count: int
    hallucination_rate: float
    hallucinated_components: List[HallucinatedComponent]
    valid_components: List[str]
    timestamp: str
    source_file: Optional[str] = None


class BedrockComponentHallucinationTracker:
    """
    Tracks and detects hallucinated Bedrock component names.

    This tracker identifies component names that don't exist in the
    Bedrock API but were generated in conversion outputs.
    """

    COMPONENT_PATTERN = re.compile(r"['\"]?(minecraft:\w+(?:\.\w+)*)['\"]?")
    EVENT_PATTERN = re.compile(r"['\"]?event['\"]?\s*:\s*['\"]?(\w+)['\"]?")
    BEHAVIOR_GOAL_PATTERN = re.compile(r"['\"]?(\w+_goal)['\"]?")

    def __init__(self, valid_components: Optional[Set[str]] = None):
        self.valid_components = valid_components or KNOWN_VALID_COMPONENTS
        self._reports: List[HallucinationReport] = []
        self._hallucination_history: List[Dict[str, Any]] = []

    def extract_components_from_text(self, text: str) -> List[str]:
        """Extract all Bedrock component names from text."""
        matches = self.COMPONENT_PATTERN.findall(text)
        return list(set(matches))

    def extract_components_from_json(self, json_str: str) -> List[str]:
        """Extract components from JSON text."""
        components = []

        component_matches = self.COMPONENT_PATTERN.findall(json_str)
        components.extend(component_matches)

        try:
            data = json.loads(json_str)
            components.extend(self._extract_from_dict(data))
        except (json.JSONDecodeError, RecursionError):
            pass

        return list(set(components))

    def _extract_from_dict(self, data: Any) -> List[str]:
        """Recursively extract components from dict/list structures."""
        components = []

        if isinstance(data, dict):
            for key, value in data.items():
                if key in ("events", "event", "add", "remove"):
                    if isinstance(value, dict):
                        components.extend(value.keys())
                if isinstance(value, str):
                    if value.startswith("minecraft:"):
                        components.append(value)
                    elif self.COMPONENT_PATTERN.match(value):
                        components.append(value)
                else:
                    components.extend(self._extract_from_dict(value))
        elif isinstance(data, list):
            for item in data:
                components.extend(self._extract_from_dict(item))

        return components

    def is_valid_component(self, component_name: str) -> bool:
        """Check if a component name is valid in Bedrock API."""
        if component_name in self.valid_components:
            return True

        if component_name.startswith("minecraft:behavior."):
            return True

        if component_name.startswith("minecraft:event."):
            return True

        return False

    def detect_hallucinations(
        self,
        text: str,
        source_file: Optional[str] = None,
    ) -> HallucinationReport:
        """
        Detect hallucinated components in text.

        Args:
            text: Text to analyze (JSON, code, or mixed)
            source_file: Optional source file path for reporting

        Returns:
            HallucinationReport with findings
        """
        components = self.extract_components_from_json(text)

        hallucinated_components = []
        valid_components = []

        for i, line in enumerate(text.split("\n"), 1):
            line_components = self.extract_components_from_text(line)
            for comp in line_components:
                if not self.is_valid_component(comp):
                    hallucination_type = self._classify_hallucination(comp)
                    hallucinated_components.append(
                        HallucinatedComponent(
                            component_name=comp,
                            line_number=i,
                            context=line.strip()[:100],
                            confidence=self._calculate_confidence(comp),
                            hallucination_type=hallucination_type,
                        )
                    )
                else:
                    valid_components.append(comp)

        hallucinated_count = len(hallucinated_components)
        total_components = len(components) if components else 1
        hallucination_rate = hallucinated_count / total_components if total_components > 0 else 0.0

        report = HallucinationReport(
            total_components=total_components,
            hallucinated_count=hallucinated_count,
            hallucination_rate=hallucination_rate,
            hallucinated_components=hallucinated_components,
            valid_components=list(set(valid_components)),
            timestamp=datetime.now().isoformat(),
            source_file=source_file,
        )

        self._reports.append(report)

        return report

    def _classify_hallucination(self, component: str) -> str:
        """Classify the type of hallucination."""
        if "ai" in component.lower() or "goal" in component.lower():
            return "fake_behavior_component"
        if "custom" in component.lower() and "event" in component.lower():
            return "fake_event_name"
        if "property" in component.lower() or "attribute" in component.lower():
            return "fake_property"
        return "unknown_component"

    def _calculate_confidence(self, component: str) -> float:
        """Calculate confidence that a component is hallucinated."""
        confidence = 0.5

        if "fake" in component.lower() or "custom_ai" in component.lower():
            confidence = 0.95
        elif "non_existent" in component.lower():
            confidence = 0.99

        return confidence

    def compare_before_after(
        self,
        before_text: str,
        after_text: str,
        label_before: str = "baseline",
        label_after: str = "uae",
    ) -> Dict[str, Any]:
        """
        Compare hallucination rates before and after a change.

        Args:
            before_text: Text before change (e.g., baseline retrieval)
            after_text: Text after change (e.g., UAE retrieval)
            label_before: Label for before text
            label_after: Label for after text

        Returns:
            Comparison metrics
        """
        before_report = self.detect_hallucinations(before_text)
        after_report = self.detect_hallucinations(after_text)

        reduction = (
            before_report.hallucination_rate - after_report.hallucination_rate
        )

        return {
            "before": {
                "label": label_before,
                "hallucination_rate": before_report.hallucination_rate,
                "hallucinated_count": before_report.hallucinated_count,
                "total_components": before_report.total_components,
            },
            "after": {
                "label": label_after,
                "hallucination_rate": after_report.hallucination_rate,
                "hallucinated_count": after_report.hallucinated_count,
                "total_components": after_report.total_components,
            },
            "reduction": {
                "absolute": reduction,
                "percent": (reduction / before_report.hallucination_rate * 100)
                if before_report.hallucination_rate > 0
                else 0.0,
            },
        }

    def get_average_hallucination_rate(self) -> float:
        """Get average hallucination rate across all reports."""
        if not self._reports:
            return 0.0

        total_rate = sum(r.hallucination_rate for r in self._reports)
        return total_rate / len(self._reports)

    def get_history(self) -> List[Dict[str, Any]]:
        """Get hallucination history."""
        return [
            {
                "timestamp": r.timestamp,
                "hallucination_rate": r.hallucination_rate,
                "hallucinated_count": r.hallucinated_count,
                "source_file": r.source_file,
            }
            for r in self._reports
        ]

    def export_history(self, path: str) -> None:
        """Export hallucination history to JSON file."""
        data = {
            "history": self.get_history(),
            "summary": {
                "total_reports": len(self._reports),
                "average_hallucination_rate": self.get_average_hallucination_rate(),
            },
        }

        with open(path, "w") as f:
            json.dump(data, f, indent=2)

        logger.info(f"Exported hallucination history to {path}")

    def reset(self) -> None:
        """Reset all reports and history."""
        self._reports = []
        self._hallucination_history = []


def create_hallucination_tracker() -> BedrockComponentHallucinationTracker:
    """Factory function to create a hallucination tracker."""
    return BedrockComponentHallucinationTracker()
