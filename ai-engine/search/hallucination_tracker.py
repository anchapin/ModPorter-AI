"""
Hallucination Tracker for Bedrock Component Names.

This module tracks and measures hallucinated Bedrock component names
in conversion outputs. Hallucinated components are names that don't exist
in the actual Bedrock API but are generated by the LLM during conversion.

Based on issue #1189: The core problem with standard RAG for code translation
is that cosine similarity retrieves Bedrock API docs that LOOK similar to a
Java query but aren't the ones that produce valid Bedrock code.

This module helps measure:
1. Hallucination rate before UAE (baseline)
2. Hallucination rate after UAE fine-tuning
3. Reduction in hallucinated component names

Common hallucination patterns:
- Wrong component names: 'minecraft:custom.ai' instead of 'minecraft:behavior.custom_goal'
- Non-existent properties: 'minecraft:entity.custom_property'
- Incorrect event names: 'minecraft:on_custom_event' instead of 'minecraft:event.my_event'
"""

