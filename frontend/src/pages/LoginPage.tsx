/**
 * Login Page
 * Provides email/password login and OAuth social login options
 * Issue #980: Add OAuth login (Discord, GitHub, Google)
 */

import React, { useState } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { authClient } from '../api/auth';
import './LoginPage.css';

interface LoginFormData {
  email: string;
  password: string;
}

export const LoginPage: React.FC = () => {
  const navigate = useNavigate();
  const [formData, setFormData] = useState<LoginFormData>({
    email: '',
    password: '',
  });
  const [error, setError] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const [oauthLoading, setOauthLoading] = useState<string | null>(null);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError(null);
    setLoading(true);

    try {
      const response = await authClient.login(formData);
      authClient.storeToken('access_token', response.access_token);
      authClient.storeToken('refresh_token', response.refresh_token);
      navigate('/dashboard');
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Login failed');
    } finally {
      setLoading(false);
    }
  };

  const handleOAuthLogin = async (
    provider: 'discord' | 'github' | 'google'
  ) => {
    setError(null);
    setOauthLoading(provider);

    try {
      const { authorization_url } =
        await authClient.getOAuthAuthorizationUrl(provider);
      window.location.href = authorization_url;
    } catch (err) {
      setError(
        err instanceof Error
          ? `Failed to start ${provider} login: ${err.message}`
          : `Failed to start ${provider} login`
      );
      setOauthLoading(null);
    }
  };

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setFormData((prev) => ({ ...prev, [e.target.name]: e.target.value }));
  };

  return (
    <div className="login-page">
      <div className="login-container">
        <div className="login-header">
          <h1>Welcome Back</h1>
          <p>Sign in to continue to ModPorter AI</p>
        </div>

        {error && <div className="login-error">{error}</div>}

        <form onSubmit={handleSubmit} className="login-form">
          <div className="form-group">
            <label htmlFor="email">Email</label>
            <input
              type="email"
              id="email"
              name="email"
              value={formData.email}
              onChange={handleChange}
              placeholder="you@example.com"
              required
              disabled={loading}
            />
          </div>

          <div className="form-group">
            <label htmlFor="password">Password</label>
            <input
              type="password"
              id="password"
              name="password"
              value={formData.password}
              onChange={handleChange}
              placeholder="Enter your password"
              required
              disabled={loading}
            />
          </div>

          <button type="submit" className="login-button" disabled={loading}>
            {loading ? 'Signing in...' : 'Sign In'}
          </button>
        </form>

        <div className="login-divider">
          <span>or continue with</span>
        </div>

        <div className="oauth-buttons">
          <button
            type="button"
            className="oauth-button oauth-discord"
            onClick={() => handleOAuthLogin('discord')}
            disabled={oauthLoading !== null}
          >
            {oauthLoading === 'discord' ? (
              'Redirecting...'
            ) : (
              <>
                <DiscordIcon />
                Continue with Discord
              </>
            )}
          </button>

          <button
            type="button"
            className="oauth-button oauth-github"
            onClick={() => handleOAuthLogin('github')}
            disabled={oauthLoading !== null}
          >
            {oauthLoading === 'github' ? (
              'Redirecting...'
            ) : (
              <>
                <GitHubIcon />
                Continue with GitHub
              </>
            )}
          </button>

          <button
            type="button"
            className="oauth-button oauth-google"
            onClick={() => handleOAuthLogin('google')}
            disabled={oauthLoading !== null}
          >
            {oauthLoading === 'google' ? (
              'Redirecting...'
            ) : (
              <>
                <GoogleIcon />
                Continue with Google
              </>
            )}
          </button>
        </div>

        <div className="login-footer">
          <p>
            Don&apos;t have an account? <Link to="/register">Sign up</Link>
          </p>
          <div className="login-legal-links">
            <Link to="/terms">Terms of Service</Link>
            <Link to="/privacy">Privacy Policy</Link>
          </div>
        </div>
      </div>
    </div>
  );
};

